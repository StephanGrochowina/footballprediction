-- results of a particular season
DROP TABLE IF EXISTS `fp_match_results`.`match_results`;
CREATE TABLE `fp_match_results`.`match_results`
(
    `match_result_id`               bigint unsigned NOT NULL AUTO_INCREMENT COMMENT 'PK',
    `season_id`                     bigint unsigned NOT NULL COMMENT 'reference to seasons.season_id',
    `match_day`                     date             NOT NULL COMMENT 'day of the match',
    `home_team_id`                  bigint unsigned NOT NULL COMMENT 'reference to teams.team_id',
    `away_team_id`                  bigint unsigned NOT NULL COMMENT 'reference to teams.team_id',
    `full_time_goals_home_team`     tinyint       NOT NULL,
    `full_time_goals_away_team`     tinyint       NOT NULL,
    `full_time_outcome`             varchar(50)      NOT NULL,
    `half_time_goals_home_team`     tinyint  DEFAULT NULL,
    `half_time_goals_away_team`     tinyint  DEFAULT NULL,
    `half_time_outcome`             varchar(50) DEFAULT NULL,
    `version`                       bigint unsigned NOT NULL,
    `home_team_shots`               int     DEFAULT NULL COMMENT 'shots by home team',
    `away_team_shots`               int     DEFAULT NULL COMMENT 'shots by away team',
    `home_team_shots_on_target`     int     DEFAULT NULL COMMENT 'shots on target by home team',
    `away_team_shots_on_target`     int     DEFAULT NULL COMMENT 'shots on target by away team',
    `home_team_corners`             int     DEFAULT NULL COMMENT 'corners for home team',
    `away_team_corners`             int     DEFAULT NULL COMMENT 'corners for away team',
    `home_team_fouls_committed`     int     DEFAULT NULL COMMENT 'fouls committed by home team',
    `away_team_fouls_committed`     int     DEFAULT NULL COMMENT 'fouls committed by away team',
    `home_team_free_kicks_conceded` int     DEFAULT NULL COMMENT 'free kicks conceded by home team',
    `away_team_free_kicks_conceded` int     DEFAULT NULL COMMENT 'free kicks conceded by away team',
    `home_team_offsides`            int     DEFAULT NULL COMMENT 'offsides by home team',
    `away_team_offsides`            int     DEFAULT NULL COMMENT 'offsides by away team',
    `home_team_yellow_cards`        int     DEFAULT NULL COMMENT 'yellow cards by home team',
    `away_team_yellow_cards`        int     DEFAULT NULL COMMENT 'yellow cards by away team',
    `home_team_red_cards`           int     DEFAULT NULL COMMENT 'red cards by home team',
    `away_team_red_cards`           int     DEFAULT NULL COMMENT 'red cards by away team',
    PRIMARY KEY (`match_result_id`),
    KEY `FK_match_results_season` (`season_id`),
    KEY `FK_match_results_home_team` (`home_team_id`),
    KEY `FK_match_results_away_team` (`away_team_id`),
    CONSTRAINT `FK_match_results_away_team` FOREIGN KEY (`away_team_id`) REFERENCES `teams` (`team_id`),
    CONSTRAINT `FK_match_results_home_team` FOREIGN KEY (`home_team_id`) REFERENCES `teams` (`team_id`),
    CONSTRAINT `FK_match_results_season` FOREIGN KEY (`season_id`) REFERENCES `seasons` (`season_id`)
) ENGINE = InnoDB DEFAULT CHARSET = utf8;
